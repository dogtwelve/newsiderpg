#include "Position3D.h"

//--------------------------------------------------------------------------
Position3D::Position3D():x(0),y(0),z(0)
//--------------------------------------------------------------------------
{
}


//--------------------------------------------------------------------------
Position3D::Position3D(int _x, int _y, int _z):x(_x),y(_y),z(_z)
//--------------------------------------------------------------------------
{
}


//--------------------------------------------------------------------------
int Position3D::Sqr_Distance(int _x, int _y , int _z)
//--------------------------------------------------------------------------
{
	return ((_x - x)*(_x - x) + (_y - y)*(_y - y) + (_z - z)*(_z - z));
}

//--------------------------------------------------------------------------
int Position3D::Sqr_Distance(Position3D& rhs)
//--------------------------------------------------------------------------
{
	return ((rhs.x - x)*(rhs.x - x) + (rhs.y - y)*(rhs.y - y) + (rhs.z - z)*(rhs.z - z));
}

//--------------------------------------------------------------------------
Position3D Position3D::operator+ (const Position3D& rhs)
//--------------------------------------------------------------------------
{
	return Position3D(x+rhs.x, y+rhs.y, z+rhs.z);
}

//--------------------------------------------------------------------------
Position3D Position3D::operator- (const Position3D& rhs)
//--------------------------------------------------------------------------
{
	return Position3D(x-rhs.x, y-rhs.y, z-rhs.z);
}

//--------------------------------------------------------------------------
Position3D Position3D::operator/ (const int& rhs)
//--------------------------------------------------------------------------
{
	return Position3D(x/rhs, y/rhs, z/rhs);
}


//--------------------------------------------------------------------------
Position3D Position3D::operator* (const int& rhs)
//--------------------------------------------------------------------------
{
	return Position3D(x*rhs, y*rhs, z*rhs);
}

//--------------------------------------------------------------------------
void Position3D::Init(int _x, int _y, int _z)
//--------------------------------------------------------------------------
{
	x = _x;
	y = _y;
	z = _z;
}

//--------------------------------------------------------------------------
int Position3D::Sqr_GetLength()
//--------------------------------------------------------------------------
{
	return (x*x + y*y + z*z);
}
